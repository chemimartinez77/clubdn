function parseDate(dateStr: string): Date {
  const [day, month, year] = dateStr.split('/').map(Number);
  return new Date(Date.UTC(year, month - 1, day));
}

async function main() {
  let updated = 0;
  let notFound = 0;
  let skipped = 0;

  for (const [email, dateStr] of CSV_DATA) {
    if (!email || !dateStr) {
      skipped++;
      continue;
    }

    const user = await prisma.user.findFirst({
      where: { email: { equals: email, mode: 'insensitive' } },
      select: { id: true, name: true }
    });

    if (!user) {
      console.log(`No encontrado: ${email}`);
      notFound++;
      continue;
    }

    const membership = await prisma.membership.findUnique({
      where: { userId: user.id }
    });

    if (!membership) {
      console.log(`Sin membresía: ${email} (${user.name})`);
      notFound++;
      continue;
    }

    await prisma.membership.update({
      where: { userId: user.id },
      data: { memberSince: parseDate(dateStr) }
    });

    updated++;
  }

  console.log(`\nResumen: ${updated} actualizados, ${notFound} no encontrados, ${skipped} omitidos (sin email/fecha)`);
  await prisma.$disconnect();
}

main().catch(e => {
  console.error(e);
  prisma.$disconnect();
  process.exit(1);
});
